public class grade {
    public static void main(String[] args){
        char grade = 'B';

        if (grade == 'A') {
            System.out.println("your perfromance is very good");
        }
        else if(grade == 'B'){
            System.out.println("your performance is good");
        }
        else if(grade == 'C'){
            System.out.println("keep improving");
        }
        else{
            System.out.println("invalid grade");
        }
    }
}
